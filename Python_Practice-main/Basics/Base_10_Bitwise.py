# 1. Complement ( ~ --> tilde Operator --> Complement)
# 2. And ( & )
# 3. Or ( | )
# 4. XOR ( ^ )
# 5. Left Shift ( << )
# 6. Right Shift ( >> )


print(~17)
print(~25)
print(~41)
print(~13)
print("\n\n")


print(12 & 13)

print(12 | 13)

print(28 | 15)

print(25 | 30)

print("\n\n")


print(25 ^ 30)
print(28 ^ 15)
print("\n\n")

print(10 << 2)
print(10 >> 2)
