# 1. Decimal      -> base 10
# 2. Binary       -> base 2
# 3. Octal        -> base 8
# 4. Hexa Decimal -> base 16

a = 25

print(bin(a))   # Dec to Bin

print(0b0101)   # Bin to Dec

print(oct(a))   # Dec To Oct

print(0o31)     # Oct to Dec

print(hex(a))   # Dec to Hex

print(0x19)     # Hex to Dec

print(oct(0xf))  # Hex to Oct

print(bin(0o31))  # Oct to Bin

print(hex(0b11001))  # Bin to Hex

print(hex(0o31))    # Oct to Hex
